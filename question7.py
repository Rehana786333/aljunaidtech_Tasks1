# Define Boolean variables
is_raining = True
is_cold = False

# Evaluate logical conditions
and_result = is_raining and is_cold  # True if both are True
or_result = is_raining or is_cold    # True if at least one is True
not_result_raining = not is_raining  # Inverts the Boolean value
not_result_cold = not is_cold        # Inverts the Boolean value

# Output the results
print(f"is_raining AND is_cold: {and_result}")  
print(f"is_raining OR is_cold: {or_result}")    
print(f"NOT is_raining: {not_result_raining}")  
print(f"NOT is_cold: {not_result_cold}")       

