# Initializing a variable
a = 10
print(f"Initial value: a = {a}")

# Assignment Operators Demonstration
a += 5
print(f"After a += 5  -> a = {a}")

a -= 3
print(f"After a -= 3  -> a = {a}")

a *= 2
print(f"After a *= 2  -> a = {a}")

a /= 4
print(f"After a /= 4  -> a = {a}")

a %= 3
print(f"After a %= 3  -> a = {a}")

a = 10  # Resetting a
a //= 3
print(f"After a //= 3 -> a = {a}")

a **= 2
print(f"After a **= 2 -> a = {a}")











